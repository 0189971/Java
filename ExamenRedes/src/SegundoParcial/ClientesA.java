    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Primero tenemos que iniciar el servidor multicas para saber los puertos de los servidores disponibles
        
        int ptoflujo=Integer.parseInt(Flujo.getText());
        int ptoMulticast=Integer.parseInt(Multicast.getText());
        int ptoRecive=Integer.parseInt(recive.getText());
        
        try{
            if(descubre==null){
            descubre=new DescubreMulticastCliente(ptoMulticast);
            descubre.start();
            }
            try{
                Thread.sleep(1000);
            }catch(Exception e){}
       
        }catch(Exception e)
        {
        JOptionPane.showMessageDialog(this, "No se pudo iniciar el hilo multicast para recibir puertos");
        }
            
            try{
                Thread.sleep(5000);
            }catch(Exception e){}    
            
                a=descubre.getdirecciones();
                int puertos[]= new int[100];
                String ips[]=new String[100];
                puertos=descubre.getpuertos();
                ips=descubre.getips();
                
                for(z=0;z<a.length;z++){
                    if(a[z].charAt(0)=='x'){
                        break;
                    }
                }
            
        try{
            if(clienteArc==null){
                for(i=0;i<z;i++){
                    try{
                    clienteArc=new HiloCliente(puertos[i],ips[i],ptoRecive);
                    String ptos=String.valueOf(puertos[i]);
                    Flujo.setText(ptos);
                    clienteArc.start();
                    break;
                    }catch(Exception e){
                        System.out.println("No se pudo conectar con: "+puertos[i]+ips[i]);
                    }
                }
                
                
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"No se pudo iniciar el hilo de flujo");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

